package utilitario.buzon;

public class AvanceTrabajo {

	/**
	 * nuevas
	 */
	private int nuevas;

	/**
	 * observaciones
	 */
	private int observaciones;

	/**
	 * terminadas
	 */
	private int terminadas;

	/**
	 * ponderacionTerminadas
	 */
	private double ponderacionTerminadas;

	/**
	 * ponderacionGrupo
	 */
	private double ponderacionGrupo;

	/**
	 * @return the nuevas
	 */
	public int getNuevas() {
		return nuevas;
	}

	/**
	 * @param nuevas the nuevas to set
	 */
	public void setNuevas(int nuevas) {
		this.nuevas = nuevas;
	}

	/**
	 * @return the observaciones
	 */
	public int getObservaciones() {
		return observaciones;
	}

	/**
	 * @param observaciones the observaciones to set
	 */
	public void setObservaciones(int observaciones) {
		this.observaciones = observaciones;
	}

	/**
	 * @return the terminadas
	 */
	public int getTerminadas() {
		return terminadas;
	}

	/**
	 * @param terminadas the terminadas to set
	 */
	public void setTerminadas(int terminadas) {
		this.terminadas = terminadas;
	}

	/**
	 * @return the ponderacionTerminadas
	 */
	public double getPonderacionTerminadas() {
		return ponderacionTerminadas;
	}

	/**
	 * @param ponderacionTerminadas the ponderacionTerminadas to set
	 */
	public void setPonderacionTerminadas(double ponderacionTerminadas) {
		this.ponderacionTerminadas = ponderacionTerminadas;
	}

	/**
	 * @return the ponderacionGrupo
	 */
	public double getPonderacionGrupo() {
		return ponderacionGrupo;
	}

	/**
	 * @param ponderacionGrupo the ponderacionGrupo to set
	 */
	public void setPonderacionGrupo(double ponderacionGrupo) {
		this.ponderacionGrupo = ponderacionGrupo;
	}

}
